/**
 * 滑块控件 — 属性面板配置说明
 *
 * 滑块通过右侧属性面板配置 ON 帧，拖动时自动发送。
 * Data 字段中用特殊字节 0xFE 作为滑块值的占位符，
 * 发送时 0xFE 会被替换为滑块当前值（0~255）。
 *
 * ── 属性面板填写方式 ──────────────────────────────
 *
 * ON 帧（滑块松开时发送）：
 *   Func:  0x02
 *   Stat:  11
 *   Data:  03 FE          ← 0xFE 会被替换为滑块值
 *
 * ── 常用配置示例 ──────────────────────────────────
 *
 * 控制 LED 闪烁间隔（ActionBus 0x02，mask=0x03）：
 *   Func=0x02  Stat=11  Data=03 FE
 *   滑块值 5 → 发送 03 05，表示间隔 5×100=500ms
 *
 * 设置 PWM 占空比（0x08，通道 0，值 0~255）：
 *   Func=0x08  Stat=11  Data=00 FE
 *   滑块值 128 → 发送 00 80，约 50% 占空比
 *
 * 设置电机速度（0x0E，int8 速度 -127~127，滑块 0~255）：
 *   Func=0x0E  Stat=11  Data=FE
 *   注意：滑块输出 0~255，如需 -127~127 需在下位机转换
 *
 * 多字节参数（固定头 + 滑块值 + 固定尾）：
 *   Data=01 FE 00
 *   → 发送 01 [滑块值] 00
 *
 * ── 占位符规则 ────────────────────────────────────
 *   Data 中每个 0xFE 字节都会被替换为同一个滑块值。
 *   滑块范围固定 0~255，如需其他范围请在下位机换算。
 */
